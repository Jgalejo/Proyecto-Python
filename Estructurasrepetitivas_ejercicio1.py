#Programa que muestre los numeros multiplos de 5 de 0 a 100 utilizando primero un bucle for y luego un bucle do while
limite=100
j=0

print("Ciclo FOR\n")
for i in range(0,limite):
    if i%5==0:
        print(i+5)

print("Ciclo While\n")
while True:
    j<100
    j+=1
    if j%5==0:
        print(j)
    if j==100:
        break











